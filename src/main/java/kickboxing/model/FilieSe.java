package kickboxing.model;

public class FilieSe {
    private String nomeFilie;

    private String emailFilie;

    private String enderecoFilie;

    private String cidadeFilie;

    private String graduacaoFilie;

    private String contatoFilie;

    private String descricaoFilie;

    public String getNomeFilie() {
        return nomeFilie;
    }

    public void setNomeFilie(String nomeFilie) {
        this.nomeFilie = nomeFilie;
    }

    public String getEmailFilie() {
        return emailFilie;
    }

    public void setEmailFilie(String emailFilie) {
        this.emailFilie = emailFilie;
    }

    public String getEnderecoFilie() {
        return enderecoFilie;
    }

    public void setEnderecoFilie(String enderecoFilie) {
        this.enderecoFilie = enderecoFilie;
    }

    public String getCidadeFilie() {
        return cidadeFilie;
    }

    public void setCidadeFilie(String cidadeFilie) {
        this.cidadeFilie = cidadeFilie;
    }

    public String getGraduacaoFilie() {
        return graduacaoFilie;
    }

    public void setGraduacaoFilie(String graduacaoFilie) {
        this.graduacaoFilie = graduacaoFilie;
    }

    public String getContatoFilie() {
        return contatoFilie;
    }

    public void setContatoFilie(String contatoFilie) {
        this.contatoFilie = contatoFilie;
    }

    public String getDescricaoFilie() {
        return descricaoFilie;
    }

    public void setDescricaoFilie(String descricaoFilie) {
        this.descricaoFilie = descricaoFilie;
    }
}
